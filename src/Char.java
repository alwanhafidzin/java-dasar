public class Char {
    public static void main(String[] args) {
        char a = 'A';
        //Petik satu untuk char
        char l = 'L';
        char w = 'W';
        //bisa gunakan char dengan nilai
        char i = 123;

        System.out.println(a);
        System.out.println(l);
        System.out.println(w);
        System.out.println(i);
    }
}
