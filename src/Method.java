public class Method {
    public static void main(String[] args) {
        //method adalah block kode program yang berjalan saat kita panggil
        //Sebelumnya kita sudah menggunakn methoad println() untuk menampilkan tulisan console
        //untuk membuat methoad di java,kita bisa menggunakan kata kunci void,lalu diikuti dengan nama methoad, kurung () dan diakhiri dengan block
        //kita bisa memanggil methoad dengan menggunkan nama methoad lalu diikuti tanda kurung()
        //di bahasa pemrograman lain methoad juga disebut function
        sayHelloWord();
    }
    static void sayHelloWord(){
        System.out.println("Hello World");
    }
}
