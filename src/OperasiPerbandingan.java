public class OperasiPerbandingan {
    public static void main(String[] args) {
        //Jika benar true,salah false
        // > , < , != , == , >= ,<=

        int value1 = 100;
        int value2 = 100;

        System.out.println(value1 > value2);
        System.out.println(value1 >= value2);
        System.out.println(value1 < value2);
        System.out.println(value1 <= value2);
        System.out.println(value1 == value2);
        System.out.println(value1 != value2);
    }
}
